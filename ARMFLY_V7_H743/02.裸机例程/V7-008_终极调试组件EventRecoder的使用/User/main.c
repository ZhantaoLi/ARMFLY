#include "bsp.h"			/* 底层硬件驱动 */



/* 定义例程名和例程发布日期 */
#define EXAMPLE_NAME	"V7-终极调试组件EventRecorder的使用"
#define EXAMPLE_DATE	"2018-12-12"
#define DEMO_VER		"1.0"

static void PrintfLogo(void);
static void PrintfHelp(void);
extern void vEventRecorderTest(void);
uint8_t s_ucBuf[10] = "armfly";

/*
*********************************************************************************************************
*	函 数 名: main
*	功能说明: c程序入口
*	形    参: 无
*	返 回 值: 错误代码(无需处理)
*********************************************************************************************************
*/
int main(void)
{
	uint8_t ucKeyCode;		/* 按键代码 */
	uint32_t t0 = 0, t1 = 0, t2 = 0, t3 = 0, t4 = 0;
	
	bsp_Init();		/* 硬件初始化 */
	
	PrintfLogo();	/* 打印例程名称和版本等信息 */
	PrintfHelp();	/* 打印操作提示 */

	bsp_StartAutoTimer(0, 200);	/* 启动1个200ms的自动重装的定时器 */

	/* 测量中断周期 */
	vEventRecorderTest();
	
	/* 进入主程序循环体 */
	while (1)
	{
		bsp_Idle();		/* 这个函数在bsp.c文件。用户可以修改这个函数实现CPU休眠和喂狗 */

		/* 判断定时器超时时间 */
		if (bsp_CheckTimer(0))	
		{
			bsp_LedToggle(2);
			
			EventStartA(0);
			EventStopA(0);
			
			EventStartA(1);
			bsp_DelayMS(5);
			EventStopA(1);
			
			EventStartA(2);
			bsp_DelayMS(30);
			EventStopA(2);
			
			t0++;
			EventStartAv(3, t0, t0);
			bsp_DelayMS(30);
			EventStopAv(3, t0, t0);
		}

		/* 按键滤波和检测由后台systick中断服务程序实现，我们只需要调用bsp_GetKey读取键值即可。 */
		ucKeyCode = bsp_GetKey();	/* 读取键值, 无键按下时返回 KEY_NONE = 0 */
		if (ucKeyCode != KEY_NONE)
		{
			switch (ucKeyCode)
			{
				case KEY_DOWN_K1:			/* K1键按下 */
					t1 += 1;
					t2 += 2;
					EventRecord2(1+EventLevelAPI, t1, t2);
					t3 += 3;
					t4 += 4;
					EventRecord4(2+EventLevelOp, t1, t2, t3, t4);
					EventRecordData(3+EventLevelOp, s_ucBuf, sizeof(s_ucBuf));
					break;

				case KEY_DOWN_K2:			/* K2键按下 */
					printf("K2按键按下\r\n");
					break;

				case KEY_DOWN_K3:			/* K3键按下 */
					printf("K3按键按下\r\n");
					break;

				default:
					/* 其它的键值不处理 */
					break;
			}
		}
	}
}

